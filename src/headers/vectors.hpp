#ifndef _VECTORS_HPP_
#define _VECTORS_HPP_

// vector2 struct
struct Vector2 {
    // x coordinate
    float x;
    // y coordinate
    float y;
};

// vector3 struct
struct Vector3 {
    // x coordinate
    float x;
    // y coordinate
    float y;
    // z coordinate
    float z;
};

// vector4 struct
struct Vector4 {
    // x coordinate
    float x;
    // y coordinate
    float y;
    // z coordinate
    float z;
    // w coordinate
    float w;
};

#endif